CREATE TABLE IF NOT EXISTS class (
    id INT PRIMARY KEY AUTO_INCREMENT COMMENT '班级ID',
    class_name VARCHAR(50) NOT NULL COMMENT '班级名称',
    grade_year VARCHAR(4) NOT NULL COMMENT '年级（入学年份）',
    major VARCHAR(50) NOT NULL COMMENT '所属专业',
    class_code VARCHAR(20) UNIQUE NOT NULL COMMENT '班级编码',
    student_count INT NOT NULL DEFAULT 0 COMMENT '学生人数',
    head_teacher VARCHAR(20) COMMENT '班主任',
    classroom VARCHAR(50) COMMENT '教室位置',
    establish_date DATE NOT NULL COMMENT '成立日期',
    monitor_name VARCHAR(20) COMMENT '班长姓名',
    monitor_phone VARCHAR(20) COMMENT '班长电话',
    created_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='班级信息表';


