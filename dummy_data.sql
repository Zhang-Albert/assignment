CREATE TABLE IF NOT EXISTS city (
    id INT PRIMARY KEY AUTO_INCREMENT COMMENT '城市ID',
    city_name VARCHAR(50) NOT NULL COMMENT '城市名称',
    city_code VARCHAR(20) UNIQUE COMMENT '城市编码（如行政区划代码）',
    province VARCHAR(50) NOT NULL COMMENT '所属省份',
    country VARCHAR(30) NOT NULL DEFAULT '中国' COMMENT '所属国家',
    area_code VARCHAR(20) COMMENT '电话区号',
    postal_code VARCHAR(20) COMMENT '邮政编码',
    population BIGINT COMMENT '人口数量',
    area DECIMAL(10, 2) COMMENT '面积（单位：平方公里）',
    longitude DECIMAL(10, 6) COMMENT '经度',
    latitude DECIMAL(10, 6) COMMENT '纬度',
    description TEXT COMMENT '城市描述',
    create_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    update_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP COMMENT '更新时间'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='城市信息表';
